# range(start, stop)
for number in range(1,11):
    print(number)

# range(stop):
print("\n")
for number in range(10):
    print(number)

# range(start, stop, step):
print("\n")
for number in range(1,11,2):
    print(number)

# for reverse order
print("\n")
for number in range(10,0,-1):
    print(number)
